package ThingstoknowinJava;
public class IfElse{
    public static void main(String[] args){
        int x = 10;
        if(x > 15){
            System.out.println("x is greater than 5");
        }
        else if(x > 5){
            System.out.println("x is greater than 5 and less than 15");
        }
        else{
            System.out.println("x is less than 5");
        }
    }
}